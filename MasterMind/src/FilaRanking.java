
public class FilaRanking {
	int punts;
	String jugador;
	
	public FilaRanking(int punts, String jugador) {
		this.punts = punts;
		this.jugador = jugador;
	}
	
	public int getpunts() {
		return punts;
	}
	
	public void setpunts(int punts) {
		this.punts = punts;
	}
	
	public String getjugador() {
		return jugador;
	}
	
	public void setjugador(String jugador) {
		this.jugador = jugador;
	}
}
